function escapeHtml(unsafe) {
  if (!unsafe) return '';
  return unsafe
       .toString()
       .replace(/&/g, "&amp;")
       .replace(/</g, "&lt;")
       .replace(/>/g, "&gt;")
       .replace(/"/g, "&quot;")
       .replace(/'/g, "&#039;");
}

// Global initialization
document.addEventListener('DOMContentLoaded', () => {
  // Footer email signup
  document.querySelectorAll('.email-signup-form').forEach(function(form) {
    form.addEventListener('submit', async function(e) {
      e.preventDefault();
      var emailInput = form.querySelector('input[type="email"]');
      var statusP = form.nextElementSibling;
      var email = emailInput.value.trim();
      if (!email) return;
      try {
        await SupaEmail.subscribe({ email: email, source: window.location.pathname });
        statusP.textContent = "You're subscribed!";
        statusP.style.color = 'var(--blue)';
        emailInput.value = '';
      } catch (err) {
        statusP.textContent = err.message || 'Something went wrong.';
        statusP.style.color = 'var(--red)';
      }
    });
  });
});
